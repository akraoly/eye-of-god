class BrowserFuzzerService:
    """Browser coverage-guided fuzzer."""

    def start(self, browser: str = "chrome", module: str = "dom",
              fuzzer: str = "domato", duration_min: int = 120,
              workers: int = 2) -> Dict:
        """Lancer session de browser fuzzing."""
        job_id = str(uuid.uuid4())
        info = _BROWSERS.get(browser, _BROWSERS["chrome"])

        _JOBS[job_id] = {
            "job_id": job_id, "browser": browser, "module": module,
            "js_engine": info["engine_js"], "renderer": info["engine_render"],
            "fuzzer": fuzzer, "fuzzer_desc": _FUZZERS.get(fuzzer, fuzzer),
            "duration_min": duration_min, "workers": workers,
            "status": "running", "executions": 0, "crashes_found": 0,
            "attack_surface": info["attack_surface"],
            "simulated": True,
        }
        _CRASHES[job_id] = []
        return _JOBS[job_id]

    def stop(self, job_id: str) -> Dict:
        if job_id in _JOBS:
            _JOBS[job_id]["status"] = "stopped"
        return _JOBS.get(job_id, {"error": "not found"})

    def status(self, job_id: str) -> Dict:
        if job_id not in _JOBS:
            return {"error": "not found"}
        job = _JOBS[job_id].copy()
        if job["status"] == "running":
            job["executions"] = random.randint(5000, 200000)
            job["crashes_found"] = random.randint(0, 8)
            job["unique_crashes"] = random.randint(0, job["crashes_found"])
            job["testcases_per_sec"] = random.randint(100, 2000)
            job["coverage_edges"] = random.randint(10000, 80000)
        return job

    def get_crashes(self, job_id: str) -> List[Dict]:
        """Lister crashes browser."""
        crashes = _CRASHES.get(job_id, [])
        if not crashes:
            crashes = [self._gen_crash(job_id) for _ in range(random.randint(1, 4))]
            _CRASHES[job_id] = crashes
        return crashes

    def _gen_crash(self, job_id: str) -> Dict:
        browser = _JOBS.get(job_id, {}).get("browser", "chrome")
        cls = random.choice(_JS_CRASH_TYPES)
        crash_id = str(uuid.uuid4())
        return {
            "crash_id": crash_id, "job_id": job_id, "browser": browser,
            "type": cls["type"], "description": cls["desc"],
            "cvss": cls["cvss"], "exploitable": cls["exploitable"],
            "sandbox_escape_needed": cls["cvss"] > 9.0,
            "crash_log": [
                f"Received signal 11 SIGSEGV",
                f"#0 0x{random.randint(0,0xFFFFFFFF):08x} ({browser}::blink::ElementResolve+0x{random.randint(0,0xFF):02x})",
                f"#1 0x{random.randint(0,0xFFFFFFFF):08x} ({browser}::v8::internal::Heap::GarbageCollect+0x{random.randint(0,0xFF):02x})",
            ],
            "testcase_path": str(_OUTPUT / f"tc_{crash_id[:8]}.html"),
            "simulated": True,
        }

    def triage_crash(self, crash_id: str, job_id: str) -> Dict:
        for c in _CRASHES.get(job_id, []):
            if c["crash_id"] == crash_id:
                return {
                    "crash_id": crash_id, "exploitable": c["exploitable"],
                    "cvss": c["cvss"],
                    "sandbox_escape": c.get("sandbox_escape_needed", False),
                    "exploit_chain": ["renderer_rce", "sandbox_escape", "kernel_priv_esc"] if c["cvss"] > 9 else ["renderer_rce"],
                    "estimated_effort": "3-7 jours" if c["exploitable"] else "N/A",
                    "simulated": True,
                }
        return {"error": "crash not found"}

    def generate_poc(self, crash_id: str, job_id: str) -> Dict:
        """Générer PoC HTML/JS à partir du crash."""
        crash = next((c for c in _CRASHES.get(job_id, []) if c["crash_id"] == crash_id),
                     {"type": "JIT_TYPER_UAF", "browser": "chrome"})
        browser = _JOBS.get(job_id, {}).get("browser", "chrome")
        poc_html = _generate_poc_js(crash.get("type", "UAF"), browser, crash_id)
        poc_path = str(_OUTPUT / f"poc_{crash_id[:8]}.html")
        with open(poc_path, "w") as f:
            f.write(poc_html)
        return {
            "crash_id": crash_id,
            "poc_path": poc_path,
            "type": "html_js",
            "size": len(poc_html),
            "trigger": crash.get("type"),
            "simulated": True,
        }

    def generate_exploit(self, crash_id: str, job_id: str) -> Dict:
        """Transformer PoC en exploit complet (sandbox escape + privesc)."""
        crash = next((c for c in _CRASHES.get(job_id, []) if c["crash_id"] == crash_id),
                     {"exploitable": True, "cvss": 9.3})
        exploit_id = str(uuid.uuid4())
        if not crash.get("exploitable"):
            return {"error": "crash non exploitable", "crash_id": crash_id}
        return {
            "exploit_id": exploit_id,
            "crash_id": crash_id,
            "stages": [
                "1. Heap spray + grooming → contrôle RIP",
                "2. JIT spray → bypass DEP/ASLR",
                "3. ROP chain → arbitrary read/write",
                "4. Mojo IPC UAF → sandbox escape",
                "5. Kernel exploit (win32k / io_uring) → SYSTEM",
            ],
            "payload": "shellcode → Meterpreter HTTPS",
            "reliability": f"{random.randint(60,95)}%",
            "cve_candidate": f"CVE-2026-{random.randint(10000,99999)}",
            "simulated": True,
        }

    def list_known_cves(self, browser: str = "chrome") -> List[Dict]:
        info = _BROWSERS.get(browser, _BROWSERS["chrome"])
        return [{"cve": c, "browser": browser} for c in info.get("cves_2024", [])]
